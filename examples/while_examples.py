my_other_number = 5

# While number example
while my_other_number < 9999:
    print ("Is this high enough: " , my_other_number)
    my_other_number += 6

# Non - terminating loop
while my_other_number == 100:
    print ("It is still 100, even afterall this time")

# While else loop exammple
while my_other_number < 10:
    print ("It is less than 10: " , my_other_number)
    my_other_number += 1
else:
    print ("It is not less than ten if we keep adding 1")
